// Variables
var myVariable = "Hello"; // Example of declaring a variable with the 'var' keyword
let anotherVariable = 42; // Example of declaring a variable with the 'let' keyword
const PI = 3.14; // Example of declaring a constant variable with the 'const' keyword

// Data Types
// Primitive Data Types
let num = 42; // Example of a number variable
let greeting = "Hello, World!"; // Example of a string variable
let isTrue = true; // Example of a boolean variable
let nullValue = null; // Example of a null variable
let undefinedValue; // Example of an undefined variable

// Complex Data Types
let person = {
  name: "John",
  age: 30,
  isStudent: false
}; // Example of an object variable

let fruits = ["apple", "banana", "orange"]; // Example of an array variable
var summer = [];
summer[0] = "january"

// Operators
// Arithmetic Operators
let sum = 5 + 3; // Addition
let difference = 10 - 5; // Subtraction
let product = 4 * 6; // Multiplication
let quotient = 8 / 2; // Division
let remainder = 15 % 4; // Modulus (Remainder)

// Comparison Operators
let isEqual = 5 == "5"; // Strict equality comparison (true)
let isEqual2 = 5 === "5"; // Strict equality comparison (false)
let isNotEqual = 10 != "10"; // Strict inequality comparison (false)
let isNotEqual2 = 10 !== "10"; // Strict inequality comparison (true)
let greaterThan = 15 > 10; // Greater than comparison (true)
let lessThan = 20 < 25; // Less than comparison (true)
let greaterThanOrEqual = 30 >= 30; // Greater than or equal to comparison (true)
let lessThanOrEqual = 40 <= 35; // Less than or equal to comparison (false)

// Logical Operators
let andOperator = true && false; // Logical AND (false)
let orOperator = true || false; // Logical OR (true)
let notOperator = !true; // Logical NOT (false)

// Assignment Operators
let x = 5;
x += 3; // x is now 8
x -= 2; // x is now 6
x *= 4; // x is now 24
x /= 3; // x is now 8

// String Concatenation
let firstName = "John";
let lastName = "Doe";
let fullName = firstName + " " + lastName; // Concatenating strings ("John Doe")
